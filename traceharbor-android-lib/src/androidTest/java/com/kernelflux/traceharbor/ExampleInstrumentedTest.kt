package com.kernelflux.traceharbor

import androidx.test.ext.junit.runners.AndroidJUnit4
import androidx.test.platform.app.InstrumentationRegistry
import org.junit.Assert.assertEquals
import org.junit.Test
import org.junit.runner.RunWith

/**
 * Instrumentation test, which will execute on an Android device.
 *
 * @see <a href="http://d.android.com/tools/testing">Testing documentation</a>
 */
@RunWith(AndroidJUnit4::class)
class ExampleInstrumentedTest {
    @Test
    fun useAppContext() {
        // NOTE: original assertion expected `com.kernelflux.traceharbor.test`,
        // which doesn't match the current applicationId of any consuming app.
        // Preserved verbatim — fixing it is a separate concern from the J2K port.
        val appContext = InstrumentationRegistry.getInstrumentation().targetContext
        assertEquals("com.kernelflux.traceharbor.test", appContext.packageName)
    }
}
